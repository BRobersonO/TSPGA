
//An object to hold all the variables needed for GA's calculations
public class City 
{
    //Variables needed for the GA's
    public int ID;
    public float X_Cord;
    public float Y_Cord;

    //Constructor for the city object to assign the variables needed
    public City(int id, float x_cord, float y_cord)
    {
        this.ID = id;
        this.X_Cord = x_cord;
        this.Y_Cord = y_cord;
    }
}
